.MODEL SMALL
.STACK 64
;VARIABLES
.DATA
TITULO DB 'ADO','$'
NUMA DB 'No asiento:','$'
DEST DB 'Destino:','$'
HORA DB 'Hora de salida:'

.CODE
MAIN PROC FAR

		MOV AX,@DATA
		MOV DS,AX
	
		;LIMPIAR TODA LA PANTALLA
		MOV AX,0600H	;AH 06(RECORRIDO)
		MOV BH,27H		;BH COLOR DEL LIMPIADO DE PANTALLA(7 BLANCO SOBRE 1 AZUL)
		MOV CX,0000H	;PRIMERA COORDENADA X,Y
		MOV DX,184FH	;SEGUNDA COORDENADA X,Y
		INT 10H
		
		MOV AX,02H	;PETICION PARA COLOCAR CURSOR
		MOV BH,00	;PAGINACION
		MOV DH,02	;RENGLON	(24 EN TOTAL)
		MOV DL,37	;COLUMNA	(79 EN TOTAL)
		INT 10H		;EJECUTA LA INTERRUPCION
		
		MOV AH,09H	;PETICION PARA DESPLIEGUE DE PANTALLA
		LEA DX,TITULO	;"UNA ESPECIE DE APUNTADOR" CUANDO ESTÁ EN 
		INT 21H		;LLAMADA A LA INTERRUPCION
		
		
		
		
		
	MOV AX,4C00H
	INT 21H
MAIN ENDP

END MAIN